// Admin query functions
export async function getUsers({
  page = 1,
  limit = 10,
  emailSearch,
}: {
  page?: number;
  limit?: number;
  emailSearch?: string;
}) {
  try {
    const offset = (page - 1) * limit;
    let query = db
      .select({
        id: user.id,
        email: user.email,
        role: user.role,
        tokenLimit: user.tokenLimit,
      })
      .from(user);

    if (emailSearch) {
      query = query.where(eq(user.email, emailSearch));
    }

    return await query.limit(limit).offset(offset);
  } catch (error) {
    console.error('Failed to get users from database');
    throw error;
  }
}

export async function updateUser({
  userId,
  role,
  tokenLimit,
}: {
  userId: string;
  role?: 'user' | 'admin';
  tokenLimit?: number;
}) {
  try {
    const updateData: Partial<typeof user.$inferInsert> = {};
    if (role) updateData.role = role;
    if (tokenLimit !== undefined) updateData.tokenLimit = tokenLimit;

    return await db.update(user).set(updateData).where(eq(user.id, userId));
  } catch (error) {
    console.error('Failed to update user in database');
    throw error;
  }
}

